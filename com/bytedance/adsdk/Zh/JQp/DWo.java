import com.bytedance.adsdk.Zh.ReZ.PjT.Zh;

public class DWo {
    private final Zh JQp;
    private final PjT PjT;
    private final Zh ReZ;
    private final Zh Zh;
    private final Zh cr;

    DWo(PjT pjT0, Zh zh0, Zh zh1, Zh zh2, Zh zh3) {
        this.PjT = pjT0;
        this.Zh = zh0;
        this.ReZ = zh1;
        this.cr = zh2;
        this.JQp = zh3;
    }

    public Zh JQp() {
        return this.JQp;
    }

    public PjT PjT() {
        return this.PjT;
    }

    public Zh ReZ() {
        return this.ReZ;
    }

    public Zh Zh() {
        return this.Zh;
    }

    public Zh cr() {
        return this.cr;
    }
}

