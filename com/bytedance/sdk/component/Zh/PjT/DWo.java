package com.bytedance.sdk.component.Zh.PjT;

import com.bytedance.sdk.component.ReZ.PjT.PjT;

public class DWo {
    public long Au;
    public long JQp;
    public long PjT;
    public long ReZ;
    public long XX;
    public long Zh;
    public long cr;
    public long cz;

    public DWo(PjT pjT0) {
        if(pjT0 != null) {
            this.PjT = pjT0.cr();
            this.Zh = pjT0.fDm();
            this.ReZ = pjT0.XX();
            this.cr = pjT0.Au();
            this.JQp = pjT0.SM();
            this.XX = pjT0.DWo();
            this.Au = pjT0.xf();
            this.cz = pjT0.cz();
        }
    }
}

