package com.bytedance.adsdk.Zh.ReZ;

import android.graphics.Typeface;

public class ReZ {
    private Typeface JQp;
    private final String PjT;
    private final String ReZ;
    private final String Zh;
    private final float cr;

    public ReZ(String s, String s1, String s2, float f) {
        this.PjT = s;
        this.Zh = s1;
        this.ReZ = s2;
        this.cr = f;
    }

    public String PjT() {
        return this.PjT;
    }

    public void PjT(Typeface typeface0) {
        this.JQp = typeface0;
    }

    public String ReZ() {
        return this.ReZ;
    }

    public String Zh() {
        return this.Zh;
    }

    public Typeface cr() {
        return this.JQp;
    }
}

